    private void productInsertButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_productInsertButtonActionPerformed
        String price = priceArea.getText();

        if (!isNumeric(price)) {
            productAnnouncementMessage.setText("Please provide the type of the price correctly, as a Long");
            return;
        }
        String quantity = quantityArea.getText();
        if (!isPositiveInteger(quantity)) {
            productAnnouncementMessage.setText("Please provide the type of the price correctly, as a Long");
            return;
        }
        String catalog_number = catalog_numberArea.getText();
        if (!isValidString(catalog_number)) {
            productAnnouncementMessage.setText("Please provide the type of the catalog number correctly as a String");
            return;
        }
        String category = categoryArea.getText();
        if (!isValidString(category)) {
            productAnnouncementMessage.setText("Please provide the type of the category correctly, as a String");
            return;
        }

        Product product = new Product();
        product.setCatalogNumber(Long.valueOf(catalog_number));
        product.setCategory(category);
        product.setPrice(Long.valueOf(price));
        product.setQuantity(Long.valueOf(quantity));
        ProductDatabaseOperations productDatabaseOperations = new ProductDatabaseOperations();
        boolean success = productDatabaseOperations.insertProduct(product);

        if (success) {
            tableModel.setRowCount(0);
            displayProducts();
            catalog_numberArea.setText("");
            categoryArea.setText("");
            priceArea.setText("");
            quantityArea.setText("");

            productAnnouncementMessage.setText("Product Inserted Succesfully...");
        } else {
            productAnnouncementMessage.setText("Failed to Insert Product, Try Again...");
        }
    }//GEN-LAST:event_productInsertButtonActionPerformed

