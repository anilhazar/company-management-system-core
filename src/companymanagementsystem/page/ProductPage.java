    private void productsDeleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_productsDeleteButtonActionPerformed
        productAnnouncementMessage.setText("");
        int selectedRowIndex = productTable.getSelectedRow();

        Object primaryKeyValue = productTable.getValueAt(selectedRowIndex, 0);
        String primaryKeyValueAsString = primaryKeyValue.toString();
        if (selectedRowIndex == -1) {
            if (productTable.getRowCount() == 0) {
                productAnnouncementMessage.setText("Table is Empty...");
            } else {
                productAnnouncementMessage.setText("Please click on a row to Delete a Product from Table");
            }
        } else {
            //Product product = new Product();
            ProductDatabaseOperations productDatabaseOperations = new ProductDatabaseOperations();

            boolean success = productDatabaseOperations.deleteProduct(primaryKeyValueAsString);
            if (success) {
                tableModel.setRowCount(0);
                displayProducts();
                productAnnouncementMessage.setText("Product Successfully Deleted...");
            } else {
                productAnnouncementMessage.setText("Product Delete Attempt Failed...");
            }

        }
    }//GEN-LAST:event_productsDeleteButtonActionPerformed

