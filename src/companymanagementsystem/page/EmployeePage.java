    public boolean isEmployeeNumeric(String str) {
        try {
            Long.valueOf(str);
            return true;
        } catch (NumberFormatException e) {
            return false;
        }
    }

    public boolean isEmployeePositiveInteger(String str) {
        try {
            int intValue = Integer.parseInt(str);
            return intValue > 0;
        } catch (NumberFormatException e) {
            return false;
        }
    }

    public boolean isEmployeeValidString(String str) {
        return str != null && !str.isEmpty();
    }

