    private void employeeUpdateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_employeeUpdateButtonActionPerformed
        String employeeId = employeeIdArea.getText();
        String firstname = employeeFirstnameArea.getText();
        String surname = employeeSurnameArea.getText();
        String position = employeePositionArea.getText();

        if (!isEmployeeNumeric(employeeId)) {
            employeeAnnouncmentMessage.setText("employee_id should be a valid positive integer.");
            return;
        }

        if (!isEmployeeNumeric(firstname)) {
            employeeAnnouncmentMessage.setText("firstname should be a valid string.");
            return;
        }

        if (!isEmployeeValidString(surname)) {
            employeeAnnouncmentMessage.setText("surname should be a valid string.");
            return;
        }

        if (!isEmployeeValidString(position)) {
            employeeAnnouncmentMessage.setText("position should be a valid string.");
            return;
        }
        if (employeeId.isEmpty() || firstname.isEmpty() || surname.isEmpty() || position.isEmpty()) {

            employeeAnnouncmentMessage.setText("Please Fill All the Fields to Insert Employee Successfully...");
            return;
        }
        employeeAnnouncmentMessage.setText("");
        int selectedRowIndex = employeeTable.getSelectedRow();

        String primaryKeyValueAsString = employeeTable.getValueAt(selectedRowIndex, 0).toString();

        if (selectedRowIndex == -1) {
            if (employeeTable.getRowCount() == 0) {
                employeeAnnouncmentMessage.setText("Table is Empty...");
            } else {
                employeeAnnouncmentMessage.setText("Please click on a row to Update Employee");
            }
        } else {

            Employee employee = new Employee();
            EmployeeDatabaseOperations employeeDatabaseOperations = new EmployeeDatabaseOperations();
            employee.setId(Long.valueOf(employeeIdArea.getText()));
            employee.setFirstname(employeeFirstnameArea.getText());
            employee.setSurname(employeeSurnameArea.getText());
            employee.setPosition(employeePositionArea.getText());
            boolean isSuccess = employeeDatabaseOperations.updateEmployee(employee, primaryKeyValueAsString);
            if (isSuccess) {
                tableModel.setRowCount(0);
                displayEmployees();
                employeeAnnouncmentMessage.setText("Employee Successfully Updated...");
                employeeIdArea.setText("");
                employeeFirstnameArea.setText("");
                employeeSurnameArea.setText("");
                employeePositionArea.setText("");

                return;
            }
            employeeAnnouncmentMessage.setText("Employee Update Attempt Failed...");
            employeeIdArea.setText("");
            employeeFirstnameArea.setText("");
            employeeSurnameArea.setText("");
            employeePositionArea.setText("");

        }
    }//GEN-LAST:event_employeeUpdateButtonActionPerformed

